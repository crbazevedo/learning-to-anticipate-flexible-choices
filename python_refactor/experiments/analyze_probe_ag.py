"""W22 Probe AG — Q-velocity sensitivity analyzer.

Question: Does adding velocity-only process noise Q = diag([0, 0, σv², σv²])
to the KF improve prediction MSE and innovation whiteness on a synthetic
generator that exhibits true velocity drift?

If yes: the Probe AC+AF finding of "risk innovation lag-1 ≈ 0.5"
on production data is structurally explained, and a paper-deviation
Q-injection might be empirically valuable (subject to validating that
it doesn't regress the +7.50% breakthrough).

Usage:
    uv run python -m experiments.analyze_probe_ag \\
        --output ../docs/W22-PROBE-AG-Q-VELOCITY-SENSITIVITY.md
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

import numpy as np

from src.probes.probe_ag_q_velocity_sensitivity import compare_q_settings


def format_report(results: list[dict]) -> str:
    lines = []
    lines.append("# W22 Probe AG — Q-velocity sensitivity (synthetic KF experiment)")
    lines.append("")
    lines.append("*Auto-generated by `experiments/analyze_probe_ag.py`.*")
    lines.append("")
    lines.append("## Question")
    lines.append("")
    lines.append("Probe AC+AF found that production KF has |lag-1 autocorrelation| ≈ 0.5 on risk")
    lines.append("innovations (MODERATE/borderline MIS-TUNED), with root cause traced to")
    lines.append("`P_next = F @ P @ F.T` (no Q term). Does adding velocity-only process noise")
    lines.append("`Q = diag([0, 0, σv², σv²])` empirically improve prediction MSE and innovation")
    lines.append("whiteness on a synthetic generator that exhibits true velocity drift?")
    lines.append("")
    lines.append("## Methodology")
    lines.append("")
    lines.append("- Generator: state = [ROI, risk, ROI_vel, risk_vel] with TRUE velocity random walk.")
    lines.append("- Two KF variants: Q_zero (production-equivalent, no process noise) vs.")
    lines.append("  Q_velocity (Q = diag([0, 0, σv²·1e-6, σv²])).")
    lines.append("- T=23 time steps (matching production walk-forward period count).")
    lines.append("- Per-seed: prediction MSE + per-objective lag-1 innovation autocorrelation.")
    lines.append("")
    lines.append("## Results — sweep over σv (true velocity drift std-dev)")
    lines.append("")
    lines.append("| σv (true) | Q-zero MSE | Q-vel MSE | Δ MSE | Q-zero lag1 risk | Q-vel lag1 risk | Δ lag1 risk |")
    lines.append("|---|---|---|---|---|---|---|")
    for r in results:
        delta_mse = (r["Q_velocity"]["mse_obs_mean"] - r["Q_zero"]["mse_obs_mean"]) / r["Q_zero"]["mse_obs_mean"]
        delta_lag1 = abs(r["Q_velocity"]["lag1_autocorr_risk_mean"]) - abs(r["Q_zero"]["lag1_autocorr_risk_mean"])
        lines.append(
            f"| {r['sigma_v']:.4f} "
            f"| {r['Q_zero']['mse_obs_mean']:.4e} "
            f"| {r['Q_velocity']['mse_obs_mean']:.4e} "
            f"| {delta_mse:+.1%} "
            f"| {r['Q_zero']['lag1_autocorr_risk_mean']:+.3f} "
            f"| {r['Q_velocity']['lag1_autocorr_risk_mean']:+.3f} "
            f"| {delta_lag1:+.3f} |"
        )
    lines.append("")
    lines.append("## Per-σv detailed stats")
    lines.append("")
    for r in results:
        lines.append(f"### σv = {r['sigma_v']:.4f}")
        lines.append("")
        lines.append("| metric | Q=0 mean ± std | Q=σv² mean ± std |")
        lines.append("|---|---|---|")
        for metric in ["mse_obs", "lag1_autocorr_ROI", "lag1_autocorr_risk"]:
            z = r["Q_zero"]
            v = r["Q_velocity"]
            lines.append(
                f"| {metric} "
                f"| {z[metric + '_mean']:+.4e} ± {z[metric + '_std']:.4e} "
                f"| {v[metric + '_mean']:+.4e} ± {v[metric + '_std']:.4e} |"
            )
        lines.append("")

    # Verdict based on median results
    midpoint = results[len(results) // 2]
    delta_mse = (midpoint["Q_velocity"]["mse_obs_mean"]
                  - midpoint["Q_zero"]["mse_obs_mean"]) / midpoint["Q_zero"]["mse_obs_mean"]
    z_lag1 = abs(midpoint["Q_zero"]["lag1_autocorr_risk_mean"])
    v_lag1 = abs(midpoint["Q_velocity"]["lag1_autocorr_risk_mean"])
    lines.append("## Verdict (at midpoint σv)")
    lines.append("")
    lines.append(f"- σv (true) = {midpoint['sigma_v']:.4f}")
    lines.append(f"- Q-zero (production-equivalent): MSE = {midpoint['Q_zero']['mse_obs_mean']:.4e}, "
                  f"|lag-1 autocorr| risk = {z_lag1:.3f}")
    lines.append(f"- Q-velocity (Q = diag([0,0,σv²·1e-6, σv²])): MSE = {midpoint['Q_velocity']['mse_obs_mean']:.4e}, "
                  f"|lag-1 autocorr| risk = {v_lag1:.3f}")
    lines.append("")
    if delta_mse < -0.05 and v_lag1 < 0.3:
        lines.append("🟢 **Q-injection HELPS**: MSE reduced by >5% AND innovations are now WHITE.")
        lines.append("Adding Q = diag([0, 0, σv²·1e-6, σv²]) to production KF is a strong candidate")
        lines.append("intervention. Requires regression test that the +7.50% FTSE breakthrough is")
        lines.append("preserved (recommended: gate behind W22_NC32_VELOCITY_Q env var; smoke 5-seed).")
    elif delta_mse < -0.02:
        lines.append("🟡 **Q-injection moderately helps**: MSE reduced by 2-5%. Marginal benefit;")
        lines.append("worth follow-up empirical test on real FTSE data before paper-deviation.")
    else:
        lines.append("🔴 **Q-injection does NOT help** on this synthetic generator (MSE Δ < 2% or worse).")
        lines.append("The KF's initial P=diag([0.1, 0.1, 1000, 1000]) is already large enough that Q")
        lines.append("doesn't add meaningful uncertainty. Production tuning is OK as-is.")
    lines.append("")
    lines.append("## Caveat")
    lines.append("")
    lines.append("- Synthetic generator may not match FTSE's actual dynamics. Real validation would")
    lines.append("  require adding optional Q parameter to `kalman_filter.py` and running an opt-in")
    lines.append("  5-seed FTSE smoke. NOT done in this autonomous probe.")
    lines.append("- The optimal Q is data-dependent; this experiment only tests Q proportional to")
    lines.append("  the true (known) generator σv. Real-data σv would need to be estimated (e.g., via")
    lines.append("  Probe AF Mehra residual identification on production logs).")
    lines.append("")
    return "\n".join(lines)


def main(argv=None):
    parser = argparse.ArgumentParser()
    parser.add_argument("--output", type=Path, required=True)
    parser.add_argument("--n-seeds", type=int, default=100)
    parser.add_argument("--T", type=int, default=23)
    args = parser.parse_args(argv)

    sigma_vs = [0.001, 0.005, 0.01, 0.05, 0.1]
    results = []
    for sv in sigma_vs:
        r = compare_q_settings(T=args.T, n_seeds=args.n_seeds, sigma_v=sv)
        results.append(r)
        print(f"[probe-ag] σv={sv:.4f}  Q=0 MSE={r['Q_zero']['mse_obs_mean']:.4e} lag1_risk={r['Q_zero']['lag1_autocorr_risk_mean']:+.3f}  "
              f"Q=σv² MSE={r['Q_velocity']['mse_obs_mean']:.4e} lag1_risk={r['Q_velocity']['lag1_autocorr_risk_mean']:+.3f}",
              file=sys.stderr)

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(format_report(results))
    (args.output.parent / (args.output.stem + "_summary.json")).write_text(
        json.dumps(results, indent=2)
    )
    print(f"[probe-ag] wrote {args.output}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
