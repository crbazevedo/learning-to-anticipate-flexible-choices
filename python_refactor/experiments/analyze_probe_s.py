"""W22 Probe S — asset network centrality on real FTSE returns.

Question (per docs/W22-PROBE-S-V1-CENTRALITY.md): Are FTSE assets
organized in a hub structure? If yes, would hub-tilted portfolios
behave differently from the unrelated SMS/ASMS optima?

This is a CHARACTERIZATION probe: we don't modify the optimizer here,
just measure the centrality distribution. If the Gini coefficient is
HIGH (strong concentration), there's a hub story. If LOW, FTSE is
homogeneous and hub-tilt won't help.

Usage:
    uv run python -m experiments.analyze_probe_s \\
        --output ../docs/W22-PROBE-S-CENTRALITY-EMPIRICAL.md
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path

import numpy as np

from src.probes.probe_s_centrality import (
    build_correlation_network,
    centrality_gini,
    eigenvector_centrality,
)


def load_returns():
    """Load FTSE paper-window returns via walk_forward_report's loader."""
    from experiments.walk_forward_report import _load_paper_window_returns
    return _load_paper_window_returns()


def analyze_window(returns_matrix: np.ndarray, window_label: str,
                    top_k_edges: list[int]) -> dict:
    """Run centrality analysis on a returns matrix; return per-sparsification metrics."""
    out = {"window_label": window_label, "shape": returns_matrix.shape, "by_topk": []}
    for k in top_k_edges:
        adj = build_correlation_network(returns_matrix, top_k_edges_per_node=k)
        cent = eigenvector_centrality(adj)
        gini = centrality_gini(cent)
        # Top 5 asset ranks
        top_indices = np.argsort(cent)[-5:][::-1]
        out["by_topk"].append({
            "top_k_edges": k,
            "gini": float(gini),
            "max_centrality": float(np.max(cent)),
            "min_centrality": float(np.min(cent)),
            "top_5_asset_indices": top_indices.tolist(),
            "top_5_centralities": cent[top_indices].tolist(),
        })
    return out


def format_report(results: list[dict]) -> str:
    lines = []
    lines.append("# W22 Probe S — asset network centrality on FTSE returns")
    lines.append("")
    lines.append("*Auto-generated by `experiments/analyze_probe_s.py`.*")
    lines.append("")
    lines.append("## Question")
    lines.append("")
    lines.append("Are FTSE assets organized in a hub structure that hub-tilted portfolios could exploit?")
    lines.append("Measure: eigenvector centrality + Gini concentration.")
    lines.append("")
    lines.append("## Methodology")
    lines.append("")
    lines.append("- Load FTSE paper-window returns (~1566 days × 98 assets)")
    lines.append("- Slice into windows (full + first/last halves)")
    lines.append("- Build correlation network (dense + top-K sparsified)")
    lines.append("- Compute eigenvector centrality (power iteration)")
    lines.append("- Report Gini coefficient (concentration measure)")
    lines.append("")
    for r in results:
        lines.append(f"## Window: {r['window_label']}")
        lines.append("")
        lines.append(f"Shape: {r['shape']}")
        lines.append("")
        lines.append("| top_k_edges | Gini | max cent | min cent | top-5 asset indices |")
        lines.append("|---|---|---|---|---|")
        for entry in r["by_topk"]:
            lines.append(
                f"| {entry['top_k_edges']} | {entry['gini']:.3f} "
                f"| {entry['max_centrality']:.4f} | {entry['min_centrality']:.4f} "
                f"| {entry['top_5_asset_indices']} |"
            )
        lines.append("")
    lines.append("## Interpretation")
    lines.append("")
    # Use dense (top_k=None or large) Gini as headline
    dense_ginis = []
    for r in results:
        for entry in r["by_topk"]:
            if entry["top_k_edges"] is None or entry["top_k_edges"] >= 90:
                dense_ginis.append(entry["gini"])
    avg_gini = np.mean(dense_ginis) if dense_ginis else 0.0
    lines.append(f"- Mean Gini across windows (dense network): {avg_gini:.3f}")
    lines.append("")
    if avg_gini > 0.4:
        lines.append("🟢 **STRONG hub structure** (Gini > 0.4): FTSE has clear concentration of network influence.")
        lines.append("Hub-tilted portfolios may meaningfully differ from SMS/ASMS optima. Worth follow-up:")
        lines.append("integrate S-v1 hub tilt as an opt-in NC33 fix and run a 5-seed FTSE ablation.")
    elif avg_gini > 0.2:
        lines.append("🟡 **Moderate hub structure** (Gini in [0.2, 0.4]): some asset clustering visible.")
        lines.append("Marginal opportunity for hub-tilt; less compelling than other intervention candidates.")
    else:
        lines.append("🔴 **Homogeneous network** (Gini < 0.2): FTSE assets have similar centrality.")
        lines.append("Hub-tilt strategy has limited theoretical headroom on this dataset.")
    lines.append("")
    lines.append("## Caveat")
    lines.append("")
    lines.append("Probe S characterizes the *structure* of the asset graph; it does NOT predict whether")
    lines.append("hub-tilt would improve realized OOS hypervolume. That would require a separate run with")
    lines.append("hub-tilted portfolio weights passed through the same walk-forward harness.")
    lines.append("")
    return "\n".join(lines)


def main(argv=None):
    parser = argparse.ArgumentParser()
    parser.add_argument("--output", type=Path, required=True)
    args = parser.parse_args(argv)

    print("[probe-s] loading FTSE returns...", file=sys.stderr)
    df = load_returns()
    print(f"[probe-s] returns shape: {df.shape}", file=sys.stderr)

    returns_matrix = df.values  # (T, K)
    T, K = returns_matrix.shape

    results = []
    # Full window
    results.append(analyze_window(returns_matrix, "full", [5, 20, K - 1]))
    # First half (training-era)
    results.append(analyze_window(returns_matrix[: T // 2], "first_half",
                                    [5, 20, K - 1]))
    # Last half (eval-era — post-crisis)
    results.append(analyze_window(returns_matrix[T // 2 :], "last_half",
                                    [5, 20, K - 1]))

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(format_report(results))
    print(f"[probe-s] wrote {args.output}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
