"""W22 Probe AI + AI-2 — AMFC boundary bias analysis.

Question (Decision Point #3): Does AMFC degenerate to boundary picks
on large Pareto fronts? Does data-derived z_ref (NC30 b) fix it or
introduce a new asymmetric bias?

Methodology: synthetic-front sweep using the existing probe modules.

Usage:
    uv run python -m experiments.analyze_probe_aiboundary \\
        --output ../docs/W22-PROBE-AI-BOUNDARY-BIAS.md
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path

from src.probes.probe_ai_front_size_sensitivity import (
    analyze_front_size_sensitivity,
)
from src.probes.probe_ai2_zref_boundary_invariance import (
    analyze_zref_boundary_interaction,
)


def main(argv=None):
    parser = argparse.ArgumentParser()
    parser.add_argument("--output", type=Path, required=True)
    parser.add_argument("--n-runs", type=int, default=300)
    args = parser.parse_args(argv)

    lines = []
    lines.append("# W22 Probe AI + AI-2 — AMFC boundary bias")
    lines.append("")
    lines.append("*Auto-generated by `experiments/analyze_probe_aiboundary.py`.*")
    lines.append("")
    lines.append("## Question")
    lines.append("")
    lines.append("Does AMFC degenerate to boundary-only picks on large Pareto fronts? "
                  "Does data-derived z_ref (NC30 b) cure the bias?")
    lines.append("")

    # Probe AI: fixed z_ref baseline
    lines.append("## Probe AI — fixed z_ref (R1=0, R2=0.05)")
    lines.append("")
    lines.append(analyze_front_size_sensitivity(
        front_sizes=[2, 3, 5, 7, 10, 20, 30, 50],
        n_runs=args.n_runs,
    ))
    lines.append("")

    # Probe AI-2: fixed vs derived z_ref
    lines.append("## Probe AI-2 — fixed vs data-derived z_ref")
    lines.append("")
    lines.append(analyze_zref_boundary_interaction(
        front_sizes=[3, 5, 10, 20, 30],
        n_runs=args.n_runs,
    ))
    lines.append("")

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text("\n".join(lines))
    print(f"[probe-ai-bias] wrote {args.output}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
