"""W22 Probe AB — per-portfolio λ^K discrimination analyzer.

Question: production λ^K is solution-INVARIANT per period (one shared
residual window). Would per-portfolio λ^K (one window per portfolio)
provide meaningful discrimination — and if so, in what tracking regime?

This is a SYNTHETIC characterization: we don't have live per-portfolio
residual windows (would require modifying anticipatory_learning.py).
The probe generates synthetic windows under varying heterogeneity and
quantifies the gap between shared and per-portfolio λ^K.

Usage:
    uv run python -m experiments.analyze_probe_ab \\
        --output ../docs/W22-PROBE-AB-PER-PORTFOLIO-LAMBDA-K.md
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

import numpy as np

from src.probes.probe_ab_per_portfolio_lambda_k import (
    compare_lambda_k_modes,
    synthesize_residual_windows,
)


def sweep(heterogeneities: list[float], n_portfolios: int, K: int,
           mean_res: float, std_res: float, n_seeds: int) -> list[dict]:
    results = []
    for h in heterogeneities:
        ranges = []
        stds = []
        sig_counts = {"NEGLIGIBLE": 0, "MODEST": 0, "STRONG": 0}
        shared_means = []
        for seed in range(n_seeds):
            rng = np.random.default_rng(seed)
            windows = synthesize_residual_windows(
                n_portfolios=n_portfolios,
                window_size=K,
                mean_residual=mean_res,
                std_residual=std_res,
                heterogeneity=h,
                rng=rng,
            )
            res = compare_lambda_k_modes(windows)
            ranges.append(res["per_portfolio_range"])
            stds.append(res["per_portfolio_std"])
            sig_counts[res["discrimination_significance"]] += 1
            shared_means.append(res["shared_lambda_k"])
        results.append({
            "heterogeneity": h,
            "n_seeds": n_seeds,
            "mean_range": float(np.mean(ranges)),
            "mean_std": float(np.mean(stds)),
            "shared_lambda_k_mean": float(np.mean(shared_means)),
            "pct_NEGLIGIBLE": 100.0 * sig_counts["NEGLIGIBLE"] / n_seeds,
            "pct_MODEST": 100.0 * sig_counts["MODEST"] / n_seeds,
            "pct_STRONG": 100.0 * sig_counts["STRONG"] / n_seeds,
        })
    return results


def format_report(results: list[dict], params: dict) -> str:
    lines = []
    lines.append("# W22 Probe AB — per-portfolio λ^K discrimination")
    lines.append("")
    lines.append("*Auto-generated by `experiments/analyze_probe_ab.py`.*")
    lines.append("")
    lines.append("## Question")
    lines.append("")
    lines.append("Production λ^K (anticipative rate) is SOLUTION-INVARIANT per period — every portfolio")
    lines.append("in the population gets the SAME λ^K because there's one shared `_kf_residual_window`.")
    lines.append("Would PER-PORTFOLIO λ^K (one residual window per portfolio) give meaningful")
    lines.append("discrimination across portfolios? At what heterogeneity of true tracking?")
    lines.append("")
    lines.append("## Setup")
    lines.append("")
    lines.append(f"- n_portfolios per period: {params['n_portfolios']}")
    lines.append(f"- window size K: {params['K']}")
    lines.append(f"- population residual mean (μ_r): {params['mean_res']}")
    lines.append(f"- per-residual noise std (σ_r): {params['std_res']}")
    lines.append(f"- seeds per heterogeneity: {params['n_seeds']}")
    lines.append("- heterogeneity scale: per-portfolio mean residual ~ N(μ_r, h·μ_r), clipped at 0")
    lines.append("")
    lines.append("## Results — sweep over heterogeneity")
    lines.append("")
    lines.append("| heterogeneity (h) | shared λ^K | mean per-portfolio λ^K range | mean std | % STRONG | % MODEST | % NEGLIGIBLE |")
    lines.append("|---|---|---|---|---|---|---|")
    for r in results:
        lines.append(
            f"| {r['heterogeneity']:.2f} "
            f"| {r['shared_lambda_k_mean']:.4f} "
            f"| {r['mean_range']:.4f} "
            f"| {r['mean_std']:.4f} "
            f"| {r['pct_STRONG']:.0f}% "
            f"| {r['pct_MODEST']:.0f}% "
            f"| {r['pct_NEGLIGIBLE']:.0f}% |"
        )
    lines.append("")
    lines.append("## Interpretation")
    lines.append("")
    h0 = next((r for r in results if r["heterogeneity"] == 0.0), None)
    h_mid = results[len(results) // 2]
    h_max = results[-1]
    if h0:
        lines.append(f"- At h=0 (homogeneous tracking): per-portfolio range = {h0['mean_range']:.4f}")
        lines.append(f"  (essentially zero; shared λ^K is sufficient)")
    lines.append(f"- At h={h_mid['heterogeneity']:.2f}: range = {h_mid['mean_range']:.4f}, "
                  f"{h_mid['pct_STRONG']:.0f}% of seeds show STRONG discrimination")
    lines.append(f"- At h={h_max['heterogeneity']:.2f}: range = {h_max['mean_range']:.4f}, "
                  f"{h_max['pct_STRONG']:.0f}% of seeds show STRONG discrimination")
    lines.append("")
    if h_max["pct_STRONG"] > 80:
        verdict = ("🟢 **STRONG discrimination achievable**: at high heterogeneity (h≥1), per-portfolio λ^K")
        verdict2 = ("differentiates portfolios meaningfully. If FTSE tracks heterogeneously across portfolios")
        verdict3 = ("(which we'd need to verify with live per-portfolio residual logging), per-portfolio λ^K")
        verdict4 = ("could improve AMFC's argmax differentiation. Currently the breakthrough achieves this")
        verdict5 = ("via NC8c-v2 (position carry) instead of per-portfolio λ^K.")
        lines.extend([verdict, verdict2, verdict3, verdict4, verdict5])
    elif h_max["pct_STRONG"] > 30:
        lines.append("🟡 **MODERATE discrimination achievable** under high heterogeneity. Worth follow-up if")
        lines.append("FTSE's per-portfolio tracking is genuinely heterogeneous; marginal otherwise.")
    else:
        lines.append("🔴 **NEGLIGIBLE discrimination** even at high synthetic heterogeneity. Per-portfolio λ^K")
        lines.append("would not provide useful AMFC differentiation. Production's shared-window approach is fine.")
    lines.append("")
    lines.append("## Caveat")
    lines.append("")
    lines.append("- Synthetic heterogeneity ≠ FTSE heterogeneity. To answer empirically, we'd need to add")
    lines.append("  per-portfolio residual logging to `anticipatory_learning.py` and run a 5-seed FTSE smoke.")
    lines.append("- Probe AB ships the analyzer but not the production wiring (operator decision).")
    lines.append("- The shared-window concatenation baseline is the closest analytic stand-in for the")
    lines.append("  current production behavior; it may slightly underestimate the true shared λ^K depending")
    lines.append("  on noise normalization.")
    lines.append("")
    return "\n".join(lines)


def main(argv=None):
    parser = argparse.ArgumentParser()
    parser.add_argument("--output", type=Path, required=True)
    parser.add_argument("--n-portfolios", type=int, default=20)
    parser.add_argument("--K", type=int, default=3)
    parser.add_argument("--mean-res", type=float, default=0.01)
    parser.add_argument("--std-res", type=float, default=0.003)
    parser.add_argument("--n-seeds", type=int, default=50)
    args = parser.parse_args(argv)

    heterogeneities = [0.0, 0.1, 0.3, 0.5, 1.0, 2.0]
    results = sweep(heterogeneities, args.n_portfolios, args.K,
                      args.mean_res, args.std_res, args.n_seeds)

    args.output.parent.mkdir(parents=True, exist_ok=True)
    params = {
        "n_portfolios": args.n_portfolios,
        "K": args.K,
        "mean_res": args.mean_res,
        "std_res": args.std_res,
        "n_seeds": args.n_seeds,
    }
    args.output.write_text(format_report(results, params))
    (args.output.parent / (args.output.stem + "_summary.json")).write_text(
        json.dumps({"params": params, "results": results}, indent=2)
    )
    print(f"[probe-ab] wrote {args.output}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
