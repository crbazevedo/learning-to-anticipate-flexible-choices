"""W22 Probe L — per-period breakdown of the ASMS-SMS breakthrough.

Question: Does the +7.50% breakthrough come UNIFORMLY across the 23
walk-forward periods, or is it CONCENTRATED in specific periods
(e.g., post-2008 crisis recovery)?

Methodology:
- For each period × seed × scenario, pick AMFC weights (argmax predicted HV)
- Compute the AMFC's REALIZED HV: max(0, actual_ROI - R1) * max(0, R2 - actual_risk)
- Aggregate per period: mean ASMS realized HV, mean SMS realized HV, Δ%
- Identify periods where ASMS uniformly wins vs loses

Reads predictions.jsonl from any 5-seed or 10-seed Probe A logged smoke.

Usage:
    uv run python -m experiments.analyze_probe_l \\
        --log-path experiments/results/w22-nc8cv2-nc8d-5seed/predictions.jsonl \\
        --output ../docs/W22-PROBE-L-PER-PERIOD-BREAKDOWN.md
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

import numpy as np
import pandas as pd


R1, R2 = 0.0, 0.2


def hv_proxy(roi: float, risk: float) -> float:
    return max(roi - R1, 0.0) * max(R2 - risk, 0.0)


def load_records(log_path: Path) -> pd.DataFrame:
    rows = []
    with log_path.open() as f:
        for line in f:
            line = line.strip()
            if line:
                rows.append(json.loads(line))
    return pd.DataFrame(rows)


def pick_amfc_realized_hv(group: pd.DataFrame) -> float:
    """Pick AMFC from group (argmax pred_hv), return its actual realized HV."""
    pred_hv = group.apply(
        lambda r: hv_proxy(r["kf_predicted_ROI_t_plus_1"], r["kf_predicted_risk_t_plus_1"]),
        axis=1,
    ).values
    idx = int(np.argmax(pred_hv)) if np.any(pred_hv > 0) else 0
    row = group.iloc[idx]
    return hv_proxy(row["actual_ROI_t_plus_1"], row["actual_risk_t_plus_1"])


def analyze(df: pd.DataFrame) -> dict:
    out = {
        "n_records": int(len(df)),
        "n_scenarios": int(df["scenario"].nunique()),
        "n_seeds": int(df["seed"].nunique()),
        "n_periods": int(df["period_t"].nunique()),
    }

    # Per (scenario, seed, period) → AMFC realized HV
    asms_per_period = {}  # {(period, seed): amfc_realized_hv}
    sms_per_period = {}
    for (scenario, seed, period), grp in df.groupby(["scenario", "seed", "period_t"]):
        amfc_hv = pick_amfc_realized_hv(grp)
        if "ASMS" in scenario:
            asms_per_period[(int(period), int(seed))] = amfc_hv
        elif "SMS" in scenario:
            sms_per_period[(int(period), int(seed))] = amfc_hv

    # Aggregate per period across seeds (paired by seed)
    periods = sorted(set(p for p, _ in asms_per_period.keys()))
    seeds = sorted(set(s for _, s in asms_per_period.keys()))

    rows = []
    for p in periods:
        asms_vals = [asms_per_period[(p, s)] for s in seeds if (p, s) in asms_per_period]
        sms_vals = [sms_per_period[(p, s)] for s in seeds if (p, s) in sms_per_period]
        # Matched seeds
        paired = [(asms_per_period[(p, s)], sms_per_period[(p, s)])
                  for s in seeds
                  if (p, s) in asms_per_period and (p, s) in sms_per_period]
        if not paired:
            continue
        asms_arr = np.array([a for a, _ in paired])
        sms_arr = np.array([s for _, s in paired])
        asms_mean = float(np.mean(asms_arr))
        sms_mean = float(np.mean(sms_arr))
        delta = asms_mean - sms_mean
        delta_pct = (delta / sms_mean * 100) if sms_mean > 1e-12 else float("nan")
        n_asms_better = int(np.sum(asms_arr > sms_arr))
        rows.append({
            "period": p,
            "n_seeds": len(paired),
            "asms_amfc_mean_hv": asms_mean,
            "sms_amfc_mean_hv": sms_mean,
            "delta_hv": delta,
            "delta_pct": delta_pct,
            "n_asms_better": n_asms_better,
        })

    out["per_period"] = rows

    # Summary stats across periods
    if rows:
        deltas = np.array([r["delta_pct"] for r in rows if np.isfinite(r["delta_pct"])])
        out["periods_total"] = len(rows)
        out["periods_asms_better"] = int(np.sum(deltas > 0))
        out["periods_sms_better"] = int(np.sum(deltas < 0))
        out["delta_pct_mean"] = float(np.mean(deltas))
        out["delta_pct_median"] = float(np.median(deltas))
        out["delta_pct_max"] = float(np.max(deltas))
        out["delta_pct_min"] = float(np.min(deltas))
        out["delta_pct_std"] = float(np.std(deltas, ddof=1)) if len(deltas) > 1 else 0.0

    return out


def format_report(summary: dict) -> str:
    lines = []
    lines.append("# W22 Probe L — per-period breakdown of the ASMS-SMS breakthrough")
    lines.append("")
    lines.append("*Auto-generated by `experiments/analyze_probe_l.py`.*")
    lines.append("")
    lines.append("## Sample")
    lines.append("")
    lines.append(f"- Records: {summary['n_records']}")
    lines.append(f"- Scenarios: {summary['n_scenarios']}")
    lines.append(f"- Seeds: {summary['n_seeds']}")
    lines.append(f"- Periods: {summary['n_periods']}")
    lines.append("")

    if "per_period" not in summary or not summary["per_period"]:
        return "\n".join(lines + ["Insufficient data for per-period breakdown."])

    lines.append("## Verdict")
    lines.append("")
    if summary["periods_asms_better"] > summary["periods_sms_better"]:
        verdict = (f"🟢 **ASMS beats SMS in {summary['periods_asms_better']}/{summary['periods_total']} periods** "
                   f"(median Δ% = {summary['delta_pct_median']:+.2f}, mean = {summary['delta_pct_mean']:+.2f})")
    else:
        verdict = (f"🟡 **ASMS beats SMS in {summary['periods_asms_better']}/{summary['periods_total']} periods**, "
                   f"breakthrough not uniform across time")
    lines.append(verdict)
    lines.append("")

    lines.append("## Period-by-period breakdown")
    lines.append("")
    lines.append("| period | n_seeds | ASMS AMFC HV | SMS AMFC HV | Δ HV | Δ % | ASMS>SMS seeds |")
    lines.append("|---|---|---|---|---|---|---|")
    for r in summary["per_period"]:
        marker = " 🟢" if r["delta_pct"] > 5 else (" 🔴" if r["delta_pct"] < -5 else "")
        lines.append(
            f"| {r['period']} | {r['n_seeds']} "
            f"| {r['asms_amfc_mean_hv']:.4e} "
            f"| {r['sms_amfc_mean_hv']:.4e} "
            f"| {r['delta_hv']:+.3e} "
            f"| {r['delta_pct']:+.2f}%{marker} "
            f"| {r['n_asms_better']}/{r['n_seeds']} |"
        )
    lines.append("")

    lines.append("## Summary statistics")
    lines.append("")
    lines.append(f"- Total periods: {summary['periods_total']}")
    lines.append(f"- ASMS-better periods (Δ > 0): {summary['periods_asms_better']}")
    lines.append(f"- SMS-better periods (Δ < 0): {summary['periods_sms_better']}")
    lines.append(f"- Mean Δ%: {summary['delta_pct_mean']:+.2f}")
    lines.append(f"- Median Δ%: {summary['delta_pct_median']:+.2f}")
    lines.append(f"- Max Δ%: {summary['delta_pct_max']:+.2f}")
    lines.append(f"- Min Δ%: {summary['delta_pct_min']:+.2f}")
    lines.append(f"- Std Δ%: {summary['delta_pct_std']:.2f}")
    lines.append("")

    # Identify clusters
    lines.append("## Period clusters")
    lines.append("")
    big_wins = [r for r in summary["per_period"] if r["delta_pct"] > 10]
    big_losses = [r for r in summary["per_period"] if r["delta_pct"] < -10]
    lines.append(f"- Big ASMS wins (Δ% > +10): {len(big_wins)} periods")
    if big_wins:
        lines.append(f"  - Periods: {sorted([r['period'] for r in big_wins])}")
    lines.append(f"- Big ASMS losses (Δ% < -10): {len(big_losses)} periods")
    if big_losses:
        lines.append(f"  - Periods: {sorted([r['period'] for r in big_losses])}")
    lines.append("")

    return "\n".join(lines)


def main(argv=None):
    parser = argparse.ArgumentParser()
    parser.add_argument("--log-path", type=Path, required=True)
    parser.add_argument("--output", type=Path, required=True)
    args = parser.parse_args(argv)

    if not args.log_path.exists():
        print(f"ERROR: log file not found at {args.log_path}", file=sys.stderr)
        return 1
    df = load_records(args.log_path)
    if df.empty:
        print(f"ERROR: log file is empty at {args.log_path}", file=sys.stderr)
        return 1
    summary = analyze(df)
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(format_report(summary))
    print(f"[probe-l] wrote report to {args.output}")
    (args.output.parent / (args.output.stem + "_summary.json")).write_text(
        json.dumps(summary, indent=2)
    )
    print(f"[probe-l] wrote summary JSON")
    return 0


if __name__ == "__main__":
    sys.exit(main())
